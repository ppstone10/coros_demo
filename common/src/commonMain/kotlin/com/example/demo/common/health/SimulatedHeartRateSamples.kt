fun aggregateFiveMinuteHeartSamples(
    samples: List<Int>,
    samplesPerInterval: Int = 6
): List<HeartRateInterval> {
    require(samplesPerInterval > 0) { "samplesPerInterval must be positive" }
    return samples
        .chunked(samplesPerInterval)
        .filter { it.size == samplesPerInterval }
        .mapIndexed { index, group ->
            HeartRateInterval(
                startMinute = index * samplesPerInterval * 5,
                minimum = group.minOrNull() ?: 0,
                maximum = group.maxOrNull() ?: 0,
                average = (group.sum() + group.size / 2) / group.size
            )
        }
}
